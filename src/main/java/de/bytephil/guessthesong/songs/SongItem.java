package de.bytephil.guessthesong.songs;

public class SongItem {

    private String title;
    private String artistsText;

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getArtistsText() {
        return artistsText;
    }

    public void setArtistsText(String artistsText) {
        this.artistsText = artistsText;
    }
}
